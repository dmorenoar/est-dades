print(habilitats_jugador | habilitats_enemic)

#Intersecció (Elements comuns)
print("Intersecció (Elements comuns)")
print(habilitats_jugador & habilitats_enemic)

#Diferència (Elements que estan al set 1 i NO al set 2)
print("Diferència (Elements que estan al set 1 i NO al set 2)")
print(habilitats_jugador - habilitats_enemic)

#Diferència simètrica (Elements que no són comuns)
print("Diferència simètrica (Elements que no són comuns)")
