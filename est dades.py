'''
Concepte: Un diccionari és una estructura de dades que guarda informació en format: clau - valor
Important:
* Mutable
* NO Indexada (No té posicions numèriques)
* Accés per clau
* Pot contenir diferents tipus de dades
'''

#Creació d'un diccionari
'''
Cada element te una clau i un valor
El primer element fa referència a la clau
El segon element fa refèrencia al valor d'aquesta clau
clau : valor
Exemple:
clau : nom - valor: Gabimaru
'''
alumne = {
    "nom" : "Gabimaru",
    "edat" : 18,
    "estil" : "ninja",
    "isMale" : True
}

#Accés als valors (Si la clau no existeix dona error)
print(alumne["nom"])
print(alumne['edat'])
'''print(alumne['attack']) -> ERROR NO EXISTEIX LA CLAU ATTACK'''

#Afegir o modificar valors
alumne["edat"] = 20
alumne["nickname"] = 'El vacío'

print(alumne['nickname'])

#Eliminar elements (Si la clau no existeix dona error)
del alumne["isMale"] #Opció 1
alumne.pop("edat") #Opció 2

'''
COMPROVAR ABANS D'ESBORRAR
'''
#Opció 1
if "edat" in alumne:
  print(alumne["edat"])
else:
  print("L'alumne no té l'atribut edat")

#Opció 2 (Si no el troba retorna NONE, però no peta)
print(f"Edat de l'alumne: {alumne.get('edat')}")

#Opció 2 versió millorada (Si no el troba retorna podem retornar un valor per defecta i no peta)
print(f"Edat de l'alumne: {alumne.get('edat',"No definit")}")
print(f"Edat de l'alumne: {alumne.get('edat',0)}")

#Recórrer un diccionari
''' Recórrer per clau'''
for clau in alumne:
  print(clau)

'''Recórrer per valor'''
for valor in alumne.values():
  print(valor)

'''Recórrer per clau-valor'''
for clau, valor in alumne.items():
  print(f"{clau} : {valor}")

